package com.project.OnlineShop.Request;

public class FavoriteProductRequest {
    int userId;
    int productId;

    public FavoriteProductRequest(int userId, int productId) {
        this.userId = userId;
        this.productId = productId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getProductId() {
        return productId;
    }

    public void setProductId(int productId) {
        this.productId = productId;
    }

}
